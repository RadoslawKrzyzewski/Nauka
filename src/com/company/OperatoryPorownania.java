package com.company;

public class OperatoryPorownania {

    public static void main(String[] args) {
        boolean zmienna;

        zmienna = 5 == 2;           // dwie liczby równe
        zmienna = 5 != 2;           // dwie liczby różne
        zmienna = 2 > 2;            // czy 2 jest większe od 2
        zmienna = 2 < 2;            // czy 2 jest mniejsze od 2
        zmienna = 2 >= 2;           // czy 2 jest większe lub równe od 2
        zmienna = 2 <= 2;            // czy 2 jest mniejsze lub równe od 2

        System.out.println(zmienna);

    }
}
